#An anonymous function is a function without a name. The following shows how to define an anonymous function
#You can use lambda function in filter() filter() function is used to filter a given iterable (list like object) using another function that defines the filtering logic
f=lambda a,b:a+b
g=lambda c,d:c*d

result=f(9,9)
result1=g(9,9)
print(result,result1)
